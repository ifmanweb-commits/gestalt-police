"""
Экземпляры VK API для разных задач.

Разделение токенов:
- group_api: групповой токен для модерации, команд, вопросов
- user_api: пользовательский токен только для публикации постов от имени сообщества
"""
import os
from vkbottle import API

# Групповой токен — для модерации, команд, вопросов, ответов экспертам
group_api = API(os.getenv('VK_TOKEN'))

# Пользовательский токен — только для публикации постов на стене группы
user_api = API(os.getenv('USER_TOKEN'))